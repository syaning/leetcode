class MyCalendar:

    def __init__(self):
        self.events = []

    def book(self, start, end):
        """
        :type start: int
        :type end: int
        :rtype: bool
        """
        for event in self.events:
            s, e = event
            if not (start >= e or end <= s):
                return False

        self.events.append([start, end])
        return True


